#include <fdf.h>

void	mapdel(char ***map)
{
	int i;
	int j;

	i = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			free(map[i][j]);
			map[i][j++] = NULL;
		}
		free(map[i]);
		map[i] = NULL;
		i++;
	}
}
